import Foundation

class Character {
    var name: String
    var age: Int
    var superPower: String
    var profession: String
    
    init(name: String, age: Int, superPower: String, profession: String) {
        self.name = name
        self.age = age
        self.superPower = superPower
        self.profession = profession
    }
    
}


